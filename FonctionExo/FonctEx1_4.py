# Exercices 1 à 4 - Opérations Mathématiques :
# Créez une série de quatre fonctions mathématiques. 
# Chacune de ces fonctions prendra deux nombres 
# en paramètres et effectuera une opération mathématique 
# différente. Vous devez implémenter 
# les opérations suivantes : 
# addition, soustraction, multiplication et division. 
# Chacune de ces fonctions renverra le résultat 
# de l'opération correspondante.
def add(a, b):   
    sum1 = a + b
    return sum1
def sous(a, b):
    sous1 = a - b
    return sous1 
def mult(a, b):
    mult1 = a * b
    return mult1
def divi(a, b):
    divi1 = a / b
    return divi1

print(add(2, 3))
print(sous(3, 2))
print(mult(3, 2))
print(divi(3, 2))
